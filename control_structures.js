let randomInt = Math.floor(Math.random() * 5);

switch(randomInt) {
    case 1:
        console.log("Der Wert der Zahl ist exakt 1");
        break;
    case 3:
        console.log("Der Wert der Zahl ist exakt 3");
        break;
    default:
        console.log("Der Wert der Zahl ist weder 1 noch 3");
}

